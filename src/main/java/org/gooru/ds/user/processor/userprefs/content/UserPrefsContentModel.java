package org.gooru.ds.user.processor.userprefs.content;

/**
 * @author ashish on 12/1/18.
 */
class UserPrefsContentModel {
  private Float webpage;
  private Float interactive;
  private Float text;
  private Float audio;
  private Float video;
  private Float image;

  public Float getWebpage() {
    return webpage;
  }

  public void setWebpage(Float webpage) {
    this.webpage = webpage;
  }

  public Float getInteractive() {
    return interactive;
  }

  public void setInteractive(Float interactive) {
    this.interactive = interactive;
  }

  public Float getText() {
    return text;
  }

  public void setText(Float text) {
    this.text = text;
  }

  public Float getAudio() {
    return audio;
  }

  public void setAudio(Float audio) {
    this.audio = audio;
  }

  public Float getVideo() {
    return video;
  }

  public void setVideo(Float video) {
    this.video = video;
  }

  public Float getImage() {
    return image;
  }

  public void setImage(Float image) {
    this.image = image;
  }

}
