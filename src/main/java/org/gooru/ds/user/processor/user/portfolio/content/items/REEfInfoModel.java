package org.gooru.ds.user.processor.user.portfolio.content.items;

/**
 * @author renuka
 */
public class REEfInfoModel {

  private String id;
  private Float efficacy;
  private Float engagement;
  private Float relevance;

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public Float getEfficacy() {
    return efficacy;
  }

  public void setEfficacy(Float efficacy) {
    this.efficacy = efficacy;
  }

  public Float getEngagement() {
    return engagement;
  }

  public void setEngagement(Float engagement) {
    this.engagement = engagement;
  }

  public Float getRelevance() {
    return relevance;
  }

  public void setRelevance(Float relevance) {
    this.relevance = relevance;
  }

}
